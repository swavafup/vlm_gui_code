def compute_metrics(results: list[PredictionResult], threshold: float = 0.5) -> dict:
    """Compute aggregate metrics from a list of PredictionResult."""
    valid = [r for r in results if r.true_label is not None and r.probability_crack is not None and not r.error]
    if not valid:
        return {"n_valid": 0}

    y_true = np.array([r.true_label for r in valid], dtype=int)
    y_prob = np.array([r.probability_crack for r in valid], dtype=float)
    y_pred = (y_prob >= threshold).astype(int)

    out: dict = {
        "n_valid": len(valid),
        "n_total": len(results),
        "threshold": threshold,
        "accuracy": float(accuracy_score(y_true, y_pred)),
    }

    # Need both classes present for some metrics
    both_classes_true = len(set(y_true.tolist())) == 2
    both_classes_pred = len(set(y_pred.tolist())) == 2

    out["precision"] = float(precision_score(y_true, y_pred, zero_division=0))
    out["recall_sensitivity"] = float(recall_score(y_true, y_pred, zero_division=0))
    out["f1"] = float(fbeta_score(y_true, y_pred, beta=1.0, zero_division=0))
    out["f2"] = float(fbeta_score(y_true, y_pred, beta=2.0, zero_division=0))
    out["mcc"] = float(matthews_corrcoef(y_true, y_pred)) if (both_classes_true and both_classes_pred) else 0.0

    # Specificity = TN / (TN + FP)
    cm = confusion_matrix(y_true, y_pred, labels=[LABEL_NON_CRACK, LABEL_CRACK])
    tn, fp, fn, tp = cm.ravel() if cm.size == 4 else (0, 0, 0, 0)
    out["specificity"] = float(tn / (tn + fp)) if (tn + fp) > 0 else 0.0
    out["confusion_matrix"] = cm.tolist()
    out["tp"], out["fp"], out["tn"], out["fn"] = int(tp), int(fp), int(tn), int(fn)

    # AUC metrics need both classes in y_true
    if both_classes_true:
        try:
            out["roc_auc"] = float(roc_auc_score(y_true, y_prob))
        except Exception:
            out["roc_auc"] = float("nan")
        try:
            out["pr_auc"] = float(average_precision_score(y_true, y_prob))
        except Exception:
            out["pr_auc"] = float("nan")
    else:
        out["roc_auc"] = float("nan")
        out["pr_auc"] = float("nan")

    # Token + timing aggregates
    out["total_inference_time_s"] = float(sum(r.inference_time_s for r in valid))
    out["mean_inference_time_s"] = float(np.mean([r.inference_time_s for r in valid]))
    out["total_input_tokens"] = int(sum(r.input_tokens for r in valid))
    out["total_output_tokens"] = int(sum(r.output_tokens for r in valid))
    out["total_tokens"] = int(sum(r.total_tokens for r in valid))

    return out


