def encode_image_to_data_url(path: str) -> str:
    """Read an image from disk and return a base64 data URL."""
    ext = Path(path).suffix.lower().lstrip(".")
    mime_subtype = "jpeg" if ext in {"jpg", "jpeg"} else (ext or "png")
    with open(path, "rb") as f:
        b64 = base64.b64encode(f.read()).decode("utf-8")
    return f"data:image/{mime_subtype};base64,{b64}"


def _safe_extract_json(text: str) -> dict:
    """Best-effort JSON extraction from a model response."""
    if not text:
        return {}
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    # Strip ``` fences
    cleaned = text.strip()
    if cleaned.startswith("```"):
        cleaned = cleaned.strip("`")
        if cleaned.lower().startswith("json"):
            cleaned = cleaned[4:]
        try:
            return json.loads(cleaned)
        except json.JSONDecodeError:
            pass
    # Substring between first { and last }
    s, e = text.find("{"), text.rfind("}")
    if s != -1 and e != -1 and e > s:
        try:
            return json.loads(text[s:e + 1])
        except json.JSONDecodeError:
            return {}
    return {}


def classify_image(
    client: OpenAI,
    model: str,
    image_path: str,
    system_prompt: str,
    user_prompt: str,
    request_json_mode: bool = True,
) -> tuple[dict, dict]:
    """Call the OpenAI Vision API.  Returns (parsed_json, meta)."""
    data_url = encode_image_to_data_url(image_path)
    start = time.perf_counter()

    kwargs = dict(
        model=model,
        messages=[
            {"role": "system", "content": system_prompt},
            {
                "role": "user",
                "content": [
                    {"type": "text", "text": user_prompt},
                    {"type": "image_url", "image_url": {"url": data_url}},
                ],
            },
        ],
    )
    if request_json_mode:
        kwargs["response_format"] = {"type": "json_object"}

    try:
        response = client.chat.completions.create(**kwargs)
    except TypeError:
        # Some reasoning models do not accept response_format — retry without it
        kwargs.pop("response_format", None)
        response = client.chat.completions.create(**kwargs)

    elapsed = time.perf_counter() - start

    raw = response.choices[0].message.content or ""
    usage = getattr(response, "usage", None)
    meta = {
        "elapsed": elapsed,
        "input_tokens": int(getattr(usage, "prompt_tokens", 0) or 0),
        "output_tokens": int(getattr(usage, "completion_tokens", 0) or 0),
        "total_tokens": int(getattr(usage, "total_tokens", 0) or 0),
        "raw": raw,
    }
    return _safe_extract_json(raw), meta


